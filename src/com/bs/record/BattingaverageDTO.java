package com.bs.record;

public class BattingaverageDTO {

		private String playerName;
		private String teamName;
		private String homeruns;
		private String battingaverage;
		private String rnum;
		
		
		public String getPlayerName() {
			return playerName;
		}
		public void setPlayerName(String playerName) {
			this.playerName = playerName;
		}
		public String getTeamName() {
			return teamName;
		}
		public void setTeamName(String teamName) {
			this.teamName = teamName;
		}
		public String getHomeruns() {
			return homeruns;
		}
		public void setHomeruns(String homeruns) {
			this.homeruns = homeruns;
		}
		public String getBattingaverage() {
			return battingaverage;
		}
		public void setBattingaverage(String battingaverage) {
			this.battingaverage = battingaverage;
		}
		public String getRnum() {
			return rnum;
		}
		public void setRnum(String rnum) {
			this.rnum = rnum;
		}
		
}
